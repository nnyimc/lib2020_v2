package beans;

public class Tva {
    private int tvaCode;
    private float tvaTaux;
    
    public Tva(){
    
    }

    public int getTvaCode() {
        return tvaCode;
    }

    public void setTvaCode(int tvaCode) {
        this.tvaCode = tvaCode;
    }

    public float getTvaTaux() {
        return tvaTaux;
    }

    public void setTvaTaux(float tvaTaux) {
        this.tvaTaux = tvaTaux;
    }
    
    
}
